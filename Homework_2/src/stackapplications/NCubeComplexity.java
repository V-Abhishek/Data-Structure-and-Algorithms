public class NCubeComplexity {

    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < 10; j++) {
                for (int k = 0; k < 10; k++) {
                    System.out.println("This is N^3 complexity program");
                }
            }
        }
    }
}
